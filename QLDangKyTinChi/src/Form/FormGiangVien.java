    public void LoadCacMonDaDK() {
        try {
            String strSql = "select MaLop,Tenmon,NgayHoc, SoSV, TietS, TietE from (Lopdoclap INNER JOIN MonHoc On Lopdoclap.Mamon = MonHoc.Mamon) where MaGV =?";
            PreparedStatement cs = ConSQL.connection.prepareStatement(strSql);
            cs.setString(1, MaGV);
            ResultSet rs = cs.executeQuery();
            System.out.println("");
            tableRecord.clear();
            Vector tableTitle = new Vector();
            tableTitle.add("Mã lớp độc lập");
            tableTitle.add("Tên môn");
            tableTitle.add("Ngày học");
            tableTitle.add("Số sinh viên");
            tableTitle.add("Tiết bắt đầu");
            tableTitle.add("Tiết kết thúc");
            while (rs.next()) {
                Vector k = new Vector();
                k.add(rs.getString(1));
                k.add(rs.getString(2));
                k.add(rs.getString(3));
                k.add(rs.getInt(4));
                k.add(rs.getInt(5));
                k.add(rs.getInt(6));
                tableRecord.add(k);
            }
            tableCacMonDaDangKy.setModel(new DefaultTableModel(tableRecord, tableTitle) {
                @Override
                public boolean isCellEditable(int x, int y) {
                    return false;
                }
            });


        } catch (SQLException ex) {
            Logger.getLogger(FormGiangVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

