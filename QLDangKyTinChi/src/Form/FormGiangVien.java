    private void btnDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangKyActionPerformed
        boolean check = true;
        String Ngay = cbbThu.getSelectedItem().toString();
        Integer Start = Integer.parseInt(cbbStart.getSelectedItem().toString()),
                End = Integer.parseInt(cbbEnd.getSelectedItem().toString());
        if (Start >= End) {
            JOptionPane.showMessageDialog(panelChiTiet, "Tiết bắt đầu không được lớn hơn hoặc bằng tiết kết thúc");
            check = false;
        } else {
            try {
                String sqlStr = "select dbo.DK_Check_GV(?,?,?,?)";
                PreparedStatement ps = ConSQL.connection.prepareStatement(sqlStr);
                ps.setString(1, MaGV);
                ps.setString(2, Ngay);
                ps.setInt(3, Start);
                ps.setInt(4, End);
                ResultSet rs = ps.executeQuery();
                rs.next();
                if (rs.getBoolean(1) == false) {
                    check = false;
                    JOptionPane.showMessageDialog(null, "Môn này bị trùng tiết với môn đã đăng ký trước đó \n Xin kiểm tra lại");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(panelChiTiet, "Lỗi! Đăng ký thất bại do lỗi SQL");
                Logger.getLogger(FormGiangVien.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (check == true) {
                String s = MaGV + lbMaMon.getText();
                LopDocLap lop = new LopDocLap(s, lbMaMon.getText(), MaGV, Ngay, Start, End, 0);
                LopAction.Them(lop, lbTenMon.getText());
            }
        }

    }//GEN-LAST:event_btnDangKyActionPerformed

