    private void btnThemSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSVActionPerformed

        if (btnThemSV.getText().equals("Thêm")) {

            SinhVien sv = new SinhVien(txtmaSV.getText(),
                    txtHoSV.getText(),
                    txtTenSV.getText(),
                    txtNienKhoa.getText(),
                    txtLopSV.getText(),
                    txtKhoaSV.getText());
            SinhVienAction svac = new SinhVienAction();
            if(svac.AddNew(sv)>0)
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            else JOptionPane.showMessageDialog(null, "Xảy ra lỗi khi thêm mới!");
            this.dispose();
        }
        else
        {
            SinhVien sv = new SinhVien(txtmaSV.getText(),
                    txtHoSV.getText(),
                    txtTenSV.getText(),
                    txtNienKhoa.getText(),
                    txtLopSV.getText(),
                    txtKhoaSV.getText());
            SinhVienAction svac = new SinhVienAction();
            if(svac.Edit(sv, sv.getMa())>0)
                JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
            else JOptionPane.showMessageDialog(null, "Xảy ra lỗi khi sửa!");
            this.dispose();
        }

    }//GEN-LAST:event_btnThemSVActionPerformed

