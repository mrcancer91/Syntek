    public DialogSinhVien(java.awt.Frame parent, boolean modal, SinhVien sv) {
        super(parent, modal);
        initComponents();
        if (null != sv) {
            this.sinhvien = sv;
            this.txtmaSV.setText(sinhvien.getMa());
            txtHoSV.setText(sinhvien.getHo());
            txtTenSV.setText(sinhvien.getTen());
            txtLopSV.setText(sinhvien.getLop());
            txtNienKhoa.setText(sinhvien.getNien_Khoa());
            txtKhoaSV.setText(sinhvien.getKhoa());
            btnThemSV.setText("Sửa");
        } else {
            try {
                Statement stm = ConSQL.connection.createStatement();
                ResultSet rs = stm.executeQuery("select top 1 MaSV from SinhVien order by MaSV desc ");
                if (rs.next()) {
                    String ma = rs.getString(1);
                    ma=ma.trim();
                    Integer masv = Integer.parseInt(ma);
                    masv++;
                    this.txtmaSV.setText(masv.toString());
                    
                }
                btnThemSV.setText("Thêm");
            } catch (SQLException ex) {
                Logger.getLogger(DialogSinhVien.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }

