    private void btnThemMonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMonActionPerformed
        String action = btnThemMon.getText();
        if ("Sửa".equals(action)) {
            mon.setMaMon(txtMaMon.getText());
            mon.setTenMon(txtTenMon.getText());
            mon.setLoaiMon(txtLoaiMon.getText());
            mon.setHeSo(Float.parseFloat(txtHeSo.getText()));
            mon.setKhoa(txtKhoa.getText());
            mon.setSoTinChi(Integer.parseInt(txtSoTinChi.getText()));
            mon.setKyDuKien(Integer.parseInt(txtKyDuKien.getText()));
            MonHocAction ac = new MonHocAction();
            int re = ac.Sua(mon, txtMaMon.getText());
            if (re > 0) {
                JOptionPane.showMessageDialog(rootPane, "Sửa thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Lỗi khi sửa", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            mon.setMaMon(txtMaMon.getText());
            mon.setTenMon(txtTenMon.getText());
            mon.setLoaiMon(txtLoaiMon.getText());
            mon.setHeSo(Float.parseFloat(txtHeSo.getText()));
            mon.setKhoa(txtKhoa.getText());
            mon.setSoTinChi(Integer.parseInt(txtSoTinChi.getText()));
            mon.setKyDuKien(Integer.parseInt(txtKyDuKien.getText()));
            MonHocAction ac = new MonHocAction();
            int re = ac.Them(mon);
            if (re > 0) {
                JOptionPane.showMessageDialog(rootPane, "Thêm môn học mới thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Lỗi khi thêm", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThemMonActionPerformed

