    public DialogMonHoc(java.awt.Frame parent, boolean modal, MonHoc monhoc) {
        super(parent, modal);
        initComponents();
        this.mon = new MonHoc();
        if (null != monhoc) {
            this.mon = monhoc;
            txtMaMon.setText(mon.getMaMon());
            txtTenMon.setText(mon.getTenMon());
            txtLoaiMon.setText(mon.getLoaiMon());
            txtHeSo.setText(String.valueOf(mon.getHeSo()));
            txtKyDuKien.setText(String.valueOf(mon.getKyDuKien()));
            txtSoTinChi.setText(String.valueOf(mon.getSoTinChi()));
            txtKhoa.setText(mon.getKhoa());
            btnThemMon.setText("Sửa");
        } else {
            try {

                Statement stm = ConSQL.connection.createStatement();
                ResultSet rs = stm.executeQuery("select top 1 Mamon from MonHoc order by Mamon desc ");
                if (rs.next()) {
                    String ma = rs.getString(1);
                    ma = ma.trim();
                    Integer masv = Integer.parseInt(ma);
                    masv++;
                    this.txtMaMon.setText(masv.toString());
                }
                btnThemMon.setText("Thêm");
            } catch (SQLException ex) {
                Logger.getLogger(DialogSinhVien.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

